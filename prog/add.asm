; ============================================================
; add.asm — Cộng hai số Mem[10]=5, Mem[11]=7, lưu Mem[12]
; Kết quả đúng: Mem[12] = 12 = 0xC
; ============================================================
LOAD  10      ; R0 = Mem[10] = 5
MOV   R1, R0  ; R1 = R0 = 5
LOAD  11      ; R0 = Mem[11] = 7
ADD   R1, R0  ; R1 = 5 + 7 = 12
MOV   R0, R1  ; R0 = 12
STORE 12      ; Mem[12] = 12
OUT   R0       ; OUT = 12
HALT
